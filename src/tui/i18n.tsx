const textByLanguage: Record<TuiLanguage, Record<TuiTextKey, string>> = {
  en: englishText,
  "zh-CN": chineseText,
}

export function isTuiLanguage(value: unknown): value is TuiLanguage {
  return value === "en" || value === "zh-CN"
}

export function translate(language: TuiLanguage, key: TuiTextKey, params?: Record<string, string | number | boolean | undefined>) {
  const template = textByLanguage[language]?.[key] ?? englishText[key] ?? key
  return template.replace(/\{(\w+)\}/g, (match, name: string) => {
    const value = params?.[name]
    return value === undefined ? match : String(value)
  })
}

type TuiI18nContextValue = {
  language: TuiLanguage
  t: (key: TuiTextKey, params?: Record<string, string | number | boolean | undefined>) => string
}

const TuiI18nContext = createContext<TuiI18nContextValue>({
  language: defaultTuiLanguage,
  t: (key, params) => translate(defaultTuiLanguage, key, params),
})

export function TuiI18nProvider(props: { language?: TuiLanguage; children: ReactNode }) {
  const language = props.language ?? defaultTuiLanguage
  return (
    <TuiI18nContext.Provider value={{ language, t: (key, params) => translate(language, key, params) }}>
      {props.children}
    </TuiI18nContext.Provider>
  )
}

export function useTuiText() {
  return useContext(TuiI18nContext).t
}

export function useTuiLanguage() {
  return useContext(TuiI18nContext).language
}
